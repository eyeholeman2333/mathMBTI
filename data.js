// 类型查找（带 fallback）
function lookupType(code) {
  if (TYPES[code]) return { code, ...TYPES[code] };
  // 尝试匹配最相近
  const keys = Object.keys(TYPES).filter(k => k !== "DEFAULT");
  let best = null, bestScore = -1;
  for (const k of keys) {
    let score = 0;
    for (let i = 0; i < Math.min(k.length, code.length); i++) {
      if (k[i] === code[i]) score++;
    }
    if (score > bestScore) { bestScore = score; best = k; }
  }
  const t = best ? TYPES[best] : TYPES.DEFAULT;
  return { code, ...t };
}

